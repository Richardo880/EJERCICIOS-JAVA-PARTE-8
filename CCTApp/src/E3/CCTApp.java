    public Integer calculadora(Integer totalTransacciones, Integer limite){
        Integer costo = 0;
        if(totalTransacciones > limite){
            if(totalTransacciones - limite < 5){
                costo = 2;
            }else{
                costo = 5;
            }
        }
        return costo;
    }
