    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String t = jTextField2.getText();
        String l = jTextField3.getText();
        
        Integer  totalTransacciones = Integer.valueOf(t);
        Integer  limite = Integer.valueOf(l);
        
        Integer costo = calculadora(totalTransacciones, limite);
        if(costo != 0){
            JOptionPane.showMessageDialog(rootPane,"Se excedió el límite de operaciones gratuitas y el sistema generará un costo de " + costo +"% sobre el\n" +
            "monto de la transacción");
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se ha superado el limite.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

